    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $dates = new CourseDates;
        
        $dates->class_id = $request->class_id;
        $dates->course_date = $request->date_submit;
        $dates->start_time = $request->start_time;
        $dates->end_time = $request->end_time;
        $dates->added_by = Auth::User()->id;
        $dates->instructor = $request->instructor;
        
        $dates->save();
        
        return redirect('classes/'.$request->class_id);
    }

